const x = Math.floor(Math.random() * 100 + 1);
const y = Math.floor(Math.random() * 50 + 1);

// To Get The Sum
const sum = x + y;
console.log(`${x} + ${y} = ${sum}`);

// To Get The Difference
const difference = x - y;
console.log(`${x} - ${y} = ${difference}`);

// To Get The Product
const product = x * y;
console.log(`${x} * ${y} = ${product}`);

// To Get The Quotient
const quotient = x / y;
console.log(`${x} / ${y} = ${quotient}`);

// To Get The Modulus
const mod = x % y;
console.log(`${x} % ${y} = ${mod}`);
